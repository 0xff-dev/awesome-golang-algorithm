package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		inputs int
		expect int
	}{
		{"TestCase1", 6, 3},
		{"TestCase2", 7, 4},
		{"TestCase3", 8, 4},
		{"TestCase4", 9, 4},
		{"TestCase5", 10, 5},
		{"TestCase6", 252, 126},
		{"TestCase7", 1314, 659},
		{"TestCase8", 9987, 4990},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.inputs)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v",
					c.expect, got, c.inputs)
			}
		})
	}
}

