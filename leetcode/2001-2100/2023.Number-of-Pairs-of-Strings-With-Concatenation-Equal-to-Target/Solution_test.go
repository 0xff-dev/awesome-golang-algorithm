package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		nums   []string
		target string
		expect int
	}{
		{"TestCase1", []string{"777", "7", "77", "77"}, "7777", 4},
		{"TestCase2", []string{"123", "4", "12", "34"}, "1234", 2},
		{"TestCase3", []string{"1", "1", "1"}, "11", 6},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.nums, c.target)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.nums, c.target)
			}
		})
	}
}

