package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		s1, s2 string
		expect int
	}{
		{"TestCase1", "xx", "yy", 1},
		{"TestCase2", "xy", "yx", 2},
		{"TestCase3", "xx", "xy", -1},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.s1, c.s2)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.s1, c.s2)
			}
		})
	}
}

