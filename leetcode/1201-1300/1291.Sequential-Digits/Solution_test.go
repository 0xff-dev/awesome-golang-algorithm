package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name      string
		low, high int
		expect    []int
	}{
		{"TestCase1", 100, 300, []int{123, 234}},
		{"TestCase2", 1000, 13000, []int{1234, 2345, 3456, 4567, 5678, 6789, 12345}},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.low, c.high)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.low, c.high)
			}
		})
	}
}

