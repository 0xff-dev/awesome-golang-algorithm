package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name     string
		n, start int
		expect   []int
	}{
		{"TestCase1", 2, 3, []int{3, 2, 0, 1}},
		{"TestCase2", 3, 2, []int{2, 6, 7, 5, 4, 0, 1, 3}},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.n, c.start)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.n, c.start)
			}
		})
	}
}

