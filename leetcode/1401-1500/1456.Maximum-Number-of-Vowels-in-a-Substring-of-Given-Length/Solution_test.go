package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		s      string
		k      int
		expect int
	}{
		{"TestCase1", "ibpbhixfiouhdljnjfflpapptrxgcomvnb", 33, 7},
		{"TestCase2", "abciiidef", 3, 3},
		{"TestCase3", "aeiou", 4, 4},
		{"TestCase4", "aeiou", 3, 3},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.s, c.k)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.s, c.k)
			}
		})
	}
}

