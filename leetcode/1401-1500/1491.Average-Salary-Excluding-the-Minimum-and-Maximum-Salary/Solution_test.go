package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		input  []int
		expect float64
	}{
		{"TestCase1", []int{4000, 3000, 1000, 2000}, 2500.00000},
		{"TestCase2", []int{1000, 2000, 3000}, 2000.00000},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.input)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v",
					c.expect, got, c.input)
			}
		})
	}
}

