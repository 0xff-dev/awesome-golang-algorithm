package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name    string
		cookies []int
		k       int
		expect  int
	}{
		{"TestCase1", []int{8, 15, 10, 20, 8}, 2, 31},
		{"TestCase2", []int{6, 1, 3, 2, 2, 4, 1, 2}, 3, 7},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.cookies, c.k)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.cookies, c.k)
			}
		})
	}
}

