package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		inputs []int
		expect bool
	}{
		{"TestCase1", []int{993335, 993336, 993337, 993338, 993339, 993340, 993341}, false},
		{"TestCase2", []int{4, 4, 4, 5, 6}, true},
		{"TestCase3", []int{1, 1, 1, 2}, false},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.inputs)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v",
					c.expect, got, c.inputs)
			}
		})
	}
}

