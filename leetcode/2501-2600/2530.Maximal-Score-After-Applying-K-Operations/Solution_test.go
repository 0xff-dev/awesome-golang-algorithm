package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		num    []int
		k      int
		expect int64
	}{
		{"TestCase1", []int{10, 10, 10, 10, 10}, 5, 50},
		{"TestCase2", []int{1, 10, 3, 3, 3}, 3, 17},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.num, c.k)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.num, c.k)
			}
		})
	}
}

